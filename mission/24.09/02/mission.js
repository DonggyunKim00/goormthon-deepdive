// 변수 선언과 데이터 타입 확인, 다양한 연산자 사용 예제를 포함한 자바스크립트 코드 파일, 결과 스크린샷

let data;

// 원시타입(Primitive Type)
data = 'hello';
console.log(typeof data); // string

data = 123;
console.log(typeof data); // number

data = true;
console.log(typeof data); // boolean

data = null;
console.log(typeof data); // object

data = undefined;
console.log(typeof data); // undefined

data = Symbol();
console.log(typeof data); // symbol

// 참조 타입(Object Type)
data = { name: 'donggyun' };
console.log(typeof data); // object

data = [1, 2, 3, 4, 5];
console.log(typeof data); // object
